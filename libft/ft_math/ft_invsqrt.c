float	ft_invsqrt(float x)
{
	float	xhalf;
	int		i;

	xhalf = 0.5f * x;
	i = *(int *) & x;
	i = 0x5f3759df - (i >> 1);
	x = *(float *) & i;
	x = x * (1.5f - (xhalf * x * x));
	return (x);
}
